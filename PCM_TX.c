// Function to check the state of channel
void channel_check(int ch1, int ch2)
{
  if ((ch1 == 0x01) && (ch2 == 0x00))
  {
    // read data from CH1 input
    HAL_GPIO_WritePin(GPIOA, Mux_select, GPIO_PIN_RESET);
    HAL_GPIO_WritePin(GPIOB, GPIO_PIN_4, GPIO_PIN_RESET);
  }
  else if ((ch1 == 0x00) && (ch2 == 0x01))
  {
    // read data from CH2 input
    HAL_GPIO_WritePin(GPIOA, Mux_select, GPIO_PIN_SET);
    HAL_GPIO_WritePin(GPIOB, GPIO_PIN_4, GPIO_PIN_SET);
  }

  else if ((ch1 == 1) && (ch2 == 1))
  {
    // read data from CH1 & CH2 input
    HAL_Delay(1000);
    HAL_GPIO_TogglePin(GPIOB, GPIO_PIN_4);
    HAL_GPIO_TogglePin(GPIOA, Mux_select);
  }
  else if ((ch1 == 0) && (ch2 == 0))
  {
    // No data
  }

  else
  {
    // No thing
  }
}

